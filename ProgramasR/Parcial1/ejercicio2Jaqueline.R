#EJERCICIO2 DE CLASE, 10/09/2021

"Realiza una versión de la función sign. Esta función recibe un vector numérico 
y devuelve un vector del mismo tamaño que el vector original, pero con los 
valores 1 (valores positivos), 0 (valores 0) y -1 (valores negativos). 
Por ejemplo, sign(c(-4, 2, 0, 3)) devuelve el vector c(-1, 1, 0, 1)."